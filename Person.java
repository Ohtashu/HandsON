package PackageMain;

public class Person {
   private String name;
   private String contractNum;
   
   public void setName(String n)
   {
	   this.name = n;
   }
   public String getName()
   {
	   return name;
   }
   public void setContactNum(String c)
   {
	   this.contractNum = c;
   }
   public String getContactNum()
   {
	   return contractNum;
   }
}
